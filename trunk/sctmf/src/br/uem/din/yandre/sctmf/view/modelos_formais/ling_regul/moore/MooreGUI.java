    public void setModeloFormal(ModeloFormal mf) {
        Moore moore = (Moore)mf;
        this.cadAlf.setSimbAfabeto(moore.getSimbolos());
        this.cadAlf.setSimbAfabetoSaida(moore.getSimbolosSaida());
        
        this.cadEst.setEstados(moore.getEstados());
        this.cadEst.setEstadoInicial(moore.getEstadoInicial());
        this.cadEst.setEstadosFinais(moore.getEstadosFinais());
        
        this.cadFTrans.setTransicoes(moore.getTransicoes());
        
        this.tpMealy.setSelectedIndex(0);
    }

    public ModeloFormal getModeloFormal() {
        Moore moore = new Moore();

        moore.addAllSimbolos(this.cadAlf.getSimbAfabeto());
        moore.addAllSimbolosSaida(this.cadAlf.getSimbAfabetoSaida());
        
        moore.addAllEstados(this.cadEst.getEstados());
        moore.setEstadoInicial(this.cadEst.getEstadoInicial());
        moore.addAllEstFinais(this.cadEst.getEstadosFinais());
        
        moore.addAllTransicoes(this.cadFTrans.getTransicoes());
        
        return moore;
    }

    void addSimbolo(Simbolo simbolo, int cod) {
        this.cadFTrans.observer(simbolo, true, cod);
        this.cadEst.observer(simbolo, true, cod);
    }
    
    void removeSimbolo(Simbolo simbolo, int cod) {
        this.cadFTrans.observer(simbolo, false, cod);
        this.cadEst.observer(simbolo, true, cod);
    }
        
    void addEstado(Estado estado) {
        this.cadFTrans.observer(estado, true);
    }
    
    void removeEstado(Estado estado) {
        this.cadFTrans.observer(estado, false);
    }    
    
