     /**
     * Observa o add e remove dos simbolos terminais e nao terminais
     * caso o simbolo nao contenha na regra de producao, a regra e removida.
     */
    void removeSimbolo(SimboloString s) {
        // verifica as regras de producao
        String sNome = s.getNome();
       for(int rpi = 0; rpi < rpsPanel.size(); rpi++) {
           JPanel rp = rpsPanel.get(rpi);
           Component comps[] = rp.getComponents();
           if (((JLabel)comps[0]).getText().equals(sNome)) {
               rpsPanel.remove(rpi); rpi--;
               continue;
           }
           
           for (int i = 2; i < comps.length; i++) {
               if (((JLabel)comps[i]).getText().equals(s.getNome())) {
                   rpsPanel.remove(rpi); rpi--;
                   break;
               }
           }
       }
       listRProd.updateUI();
       
       // se for um nao terminal, verifica os simbolos iniciais
       if(s.getNome().equals(s.getNome().toUpperCase()))
           this.cbSimbIni.removeItem(s.getNome());
       
        this.getFormaSentencial().removeSimbolo(s);
    }
 
     private Matcher getMatcher(String str) {
        String regex = "(.+) -> (.+)";       
        
        return Pattern.compile(regex).matcher(str);
    }
    
