    private void addAction(SimboloString sLadEsq, List<SimboloString> sLadDir) {
        List<SimboloString> ladoDireito = null;
        Object sNTerm = null; 
        if(sLadEsq==null) { // transicao inserida pelo usuário
            sNTerm = this.cbEsq.getSelectedItem();
            ladoDireito = formaSentencial.getSequencia();
            
        } else { // Caso os valores vem de um arquivo
            sNTerm = sLadEsq.getNome();
            ladoDireito = sLadDir;
        }
        
        int tam = ladoDireito.size();
        if(sNTerm!=null) {
            RegraProducao regProd = new RegraProducao();
            regProd.setSimbLEsq(new SimboloString(sNTerm.toString()));
            regProd.setSimbLDireito(ladoDireito);
            if (getProducoes().contains(regProd)) return;
            
            final JPanel rp = new JPanel();
            rp.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.LEFT));
            rp.setBackground(listRProd.getBackground());
            rp.add(new JLabel(sNTerm.toString()));
            rp.add(new JLabel(" -> "));
            if (tam==0) {
                rp.add(new JLabel(GLC.LAMBDA.getNome()));
            } else {
                Color foreground = new Color(242, 64, 37);
                for (SimboloString s : ladoDireito) {
                    JLabel l = new JLabel(s.getNome());
                    foreground = new Color(foreground.getRGB()^0x00FFFFFF);
                    l.setForeground(foreground);
                    rp.add(l);
                }
            }
            rpsPanel.add(rp);
            listRProd.updateUI();
            formaSentencial.limparSequencia();
            formaSentencial.updateUI();
        }
    }
    
    private void rmvAction() {
        if (listRProd.getSelectedIndex() < 0) return;
        rpsPanel.remove(listRProd.getSelectedIndex());
        listRProd.updateUI();
    }
    
