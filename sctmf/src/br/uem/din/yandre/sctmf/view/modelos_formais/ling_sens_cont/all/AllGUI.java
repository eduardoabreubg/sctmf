    public void setModeloFormal(ModeloFormal mf) {
        ALL all = (ALL)mf;
        this.cadAlf.setSimbAfabeto(all.getAlfabeto());
        this.cadAlf.setSimbAfabetoAux(all.getAlfabetoAux());
        
        this.cadEst.setEstados(all.getEstados());
        this.cadEst.setEstadoInicial(all.getEstIni());
        this.cadEst.setEstadosFinais(all.getEstFinais());

        
        this.cadFTrans.setTransicoes(all.getTransicoes());
        
        this.tpMT.setSelectedIndex(0);
    }

    public ModeloFormal getModeloFormal() {
        ALL all = new ALL();
        all.setAlfabeto(this.cadAlf.getSimbAfabeto());
        all.setAlfabetoAux(this.cadAlf.getSimbAfabetoAux());
        
        all.setEstados(this.cadEst.getEstados());
        all.setEstIni(this.cadEst.getEstadoInicial());
        all.setEstFinais(this.cadEst.getEstadosFinais());
        
        all.setTransicoes(this.cadFTrans.getTransicoes());

        all.setEstadosND(this.cadFTrans.getEstadosND());

        return all;
    }

